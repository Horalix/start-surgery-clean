/** Compact option builder: o("a","...","b","...") */
const o = (...pairs: string[]): QOption[] => {
  const out: QOption[] = [];
  for (let i = 0; i < pairs.length; i += 2) out.push({ id: pairs[i], text: pairs[i + 1] });
  return out;
};

